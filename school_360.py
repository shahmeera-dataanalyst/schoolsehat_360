"""BP_FUNCTION"""

def bp_screening(age, gender, sbp, dbp):

    if age >= 13:

        if sbp < 120 and dbp < 80:
            return "Normal"

        return "Abnormal"

    if gender == "Male":

        cutoff_sbp = bp_boys[age]["SBP"]
        cutoff_dbp = bp_boys[age]["DBP"]

    else:

        cutoff_sbp = bp_girls[age]["SBP"]
        cutoff_dbp = bp_girls[age]["DBP"]

    if sbp >= cutoff_sbp or dbp >= cutoff_dbp:

        return "Abnormal"

    return "Normal"

