"""BMI_CATEGORY"""

def bmi_category(age, gender, bmi):

    age_months = age * 12

    if gender == "Male":
        ref = boys_ref
    else:
        ref = girls_ref

    row = ref.iloc[
        (ref["Month"] - age_months).abs().argsort()[:1]
    ]

    sd2neg = float(row["SD2neg"].iloc[0])
    sd1 = float(row["SD1"].iloc[0])
    sd2 = float(row["SD2"].iloc[0])

    if bmi < sd2neg:
        return "Underweight"

    elif bmi <= sd1:
        return "Normal"

    elif bmi <= sd2:
        return "Overweight"

    else:
        return "Obese"

"""BMI_RESULT_AND_RECOMMENDATION"""

def calculate_bmi(weight_kg, height_cm):
    return weight_kg / ((height_cm / 100) ** 2)


